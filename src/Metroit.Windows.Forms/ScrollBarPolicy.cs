namespace Metroit.Windows.Forms
{
    /// <summary>
    /// スクロールバーの表示方法を定義します。
    /// </summary>
    public enum ScrollBarPolicy
    {
        /// <summary>
        /// 必要なときに表示することを示します。
        /// </summary>
        AsNeeded,

        /// <summary>
        /// 常に表示することを示します。
        /// </summary>
        Always,

        /// <summary>
        /// 常に表示しないことを示します。
        /// </summary>
        Never
    }
}
